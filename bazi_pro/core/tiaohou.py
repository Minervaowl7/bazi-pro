def lookup_tiaohou(day_master: str, month_zhi: str) -> dict:
    """查询调候用神。

    根据日主天干和月令地支，从穷通宝鉴调候表中查找对应的调候用神。
    调候用神是命局因气候失衡时最急需的五行，属于辅助参考，
    不凌驾于格局用神之上。

    查表逻辑：
        1. 以 (day_master, month_zhi) 为键查 TIAOHOU_TABLE
        2. 若命中，将天干列表转换为五行列表（通过 GAN_WUXING 映射）
        3. 生成自然语言 note 描述

    Args:
        day_master: 日主天干（如"甲"/"乙"/.../"癸"）
        month_zhi: 月支地支（如"寅"/"卯"/.../"丑"）

    Returns:
        dict: 调候查询结果，结构如下：
            {
                'has_tiaohou': bool,           # 是否有调候记录
                'tiaohou_gan': ['丙', '癸'],   # 调候用神天干列表（第一位为主调候）
                'tiaohou_wx': ['火', '水'],    # 对应五行列表（与 tiaohou_gan 一一对应）
                'note': str,                   # 自然语言描述，如"调候用丙(火)为主，癸(水)为辅"
            }
        未命中时返回：
            {
                'has_tiaohou': False,
                'tiaohou_gan': [],
                'tiaohou_wx': [],
                'note': '穷通宝鉴未收录此日主月令组合',
            }
    """
    gans = TIAOHOU_TABLE.get((day_master, month_zhi), [])
    if not gans:
        return {
            'has_tiaohou': False,
            'tiaohou_gan': [],
            'tiaohou_wx': [],
            'note': '穷通宝鉴未收录此日主月令组合',
        }

    # 将天干列表转换为五行列表（通过 GAN_WUXING 常量映射）
    wx_list = [GAN_WUXING.get(g, '') for g in gans]
    return {
        'has_tiaohou': True,
        'tiaohou_gan': gans,
        'tiaohou_wx': wx_list,
        # 生成自然语言描述：主调候+辅调候（若有）
        'note': f'调候用{gans[0]}({wx_list[0]})为主' + (f'，{gans[1]}({wx_list[1]})为辅' if len(gans) > 1 else ''),
    }
