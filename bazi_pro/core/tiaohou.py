def lookup_tiaohou(day_master: str, month_zhi: str) -> dict:
    """查询调候用神。

    Returns:
        {
            'has_tiaohou': bool,
            'tiaohou_gan': ['丙', '癸'],  # 调候用神天干(第一位为主)
            'tiaohou_wx': ['火', '水'],   # 对应五行
            'note': str,
        }
    """
    gans = TIAOHOU_TABLE.get((day_master, month_zhi), [])
    if not gans:
        return {
            'has_tiaohou': False,
            'tiaohou_gan': [],
            'tiaohou_wx': [],
            'note': '穷通宝鉴未收录此日主月令组合',
        }

    wx_list = [GAN_WUXING.get(g, '') for g in gans]
    return {
        'has_tiaohou': True,
        'tiaohou_gan': gans,
        'tiaohou_wx': wx_list,
        'note': f'调候用{gans[0]}({wx_list[0]})为主' + (f'，{gans[1]}({wx_list[1]})为辅' if len(gans) > 1 else ''),
    }
