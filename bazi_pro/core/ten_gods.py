"""十神定义与格局方向 — 基于《子平真诠》"论十神"

本模块定义十神与日主的五行生克关系，以及各十神作为格局用神时的
顺用/逆用方向。这是格局用神推导的基础数据。

核心概念：
    - 十神（SHISHEN_WUXING_REL）：天干与日主的五行生克关系分类，
      共5大类10种：比劫(同我)、食伤(我生)、财星(我克)、官杀(克我)、印星(生我)。
    - 格局方向（PATTERN_DIRECTION）：各十神作为格局用神时，
      应采用"顺用"（生护）还是"逆用"（制化）的策略。

古籍依据：
    - 《子平真诠》"论十神"：十神分善恶，善神顺用，恶神逆用。
    - 顺用 = 生护用神：官→财印生护，印→官杀生护，财→食伤生护+比劫护财
    - 逆用 = 制化用神：杀→食伤制杀，伤→印制伤官，刃→官杀制刃

用法:
    from bazi_pro.core.ten_gods import _count_shishen_categories, _get_yongshen_direction
    counts = _count_shishen_categories('甲', ['丙', '戊', '庚'], bazi_parts)
    direction = _get_yongshen_direction('正官')  # → '财印'
"""

from bazi_pro.core.branches import CANGGAN_WEIGHT
from bazi_pro.core.constants import derive_shishen
from bazi_pro.core.hidden_stems import get_canggan

# 十神 → 五行关系映射
# 将10种具体十神归类为5大类，用于统计命局中各类十神的力量分布
# "同我"=比劫类，"我生"=食伤类，"我克"=财星类，"克我"=官杀类，"生我"=印星类
# 注意：包含复合键名（如"比劫"/"食伤"/"财星"/"官杀"/"印星"/"印比"/"官星"），
# 这些复合键用于格局名称中的十神类别引用
SHISHEN_WUXING_REL = {
    '比肩': '同我', '劫财': '同我', '比劫': '同我',
    '食神': '我生', '伤官': '我生', '食伤': '我生',
    '偏财': '我克', '正财': '我克', '财星': '我克',
    '七杀': '克我', '正官': '克我', '官杀': '克我', '官星': '克我',
    '偏印': '生我', '正印': '生我', '印星': '生我', '印比': '生我',
}


def _count_shishen_categories(day_master, gans, bazi_parts, include_canggan=False):
    """统计命局中五大类十神的数量/权重。

    将命局中的天干（及可选的藏干）按十神分类统计，
    用于判断命局中各类十神的力量分布，辅助格局判定和用神推导。

    统计规则：
        - 天干十神：每个天干权重为1.0（整数计数）
        - 藏干十神（可选）：按 CANGGAN_WEIGHT 中的气分权重折算
          （本气1.0、中气0.5、余气0.3）
        - 将10种具体十神归入5大类：官杀/财星/食伤/印星/比劫

    Args:
        day_master: 日主天干（如"甲"）
        gans: 需统计的天干列表（通常为四柱天干，排除日主自身）
        bazi_parts: 四柱列表（如["甲子", "丙寅", "戊辰", "庚午"]），
            仅在 include_canggan=True 时使用
        include_canggan: 是否统计藏干十神，默认 False。
            开启后藏干按气分权重折算计入各类十神。

    Returns:
        dict: 五大类十神的统计结果，如：
            {'官杀': 2, '财星': 1, '食伤': 0, '印星': 1, '比劫': 1}
            天干统计为整数，含藏干时可能为浮点数
    """
    counts = {'官杀': 0, '财星': 0, '食伤': 0, '印星': 0, '比劫': 0}
    # 统计天干十神（权重1.0）
    for g in gans:
        ss = derive_shishen(day_master, g)  # 推导天干g对日主的十神
        if ss in ('正官', '七杀'):
            counts['官杀'] += 1
        elif ss in ('正财', '偏财'):
            counts['财星'] += 1
        elif ss in ('食神', '伤官'):
            counts['食伤'] += 1
        elif ss in ('正印', '偏印'):
            counts['印星'] += 1
        elif ss in ('比肩', '劫财'):
            counts['比劫'] += 1
    # 统计藏干十神（可选，按权重折算）
    if include_canggan:
        for p in bazi_parts:
            if len(p) < 2:
                continue
            for cg, ql in get_canggan(p[1]):       # cg=藏干天干, ql=气分(本气/中气/余气)
                ss = derive_shishen(day_master, cg)
                weight = CANGGAN_WEIGHT.get(ql, 0)  # 本气1.0, 中气0.5, 余气0.3
                if ss in ('正官', '七杀'):
                    counts['官杀'] += weight
                elif ss in ('正财', '偏财'):
                    counts['财星'] += weight
                elif ss in ('食神', '伤官'):
                    counts['食伤'] += weight
                elif ss in ('正印', '偏印'):
                    counts['印星'] += weight
                elif ss in ('比肩', '劫财'):
                    counts['比劫'] += weight
    return counts


def _get_yongshen_direction(shishen: str) -> str:
    """获取十神作为格局用神时的顺用/逆用方向。

    格局用神方向基于《子平真诠》"论用神"：
        - 顺用（善神）：生护用神，使其力量更稳固
          · 正官格 → 用财生官、用印护官（方向="财印"）
          · 正印格 → 用官杀生印（方向="官比"）
          · 偏印格 → 同正印（方向="官比"）
          · 正财格 → 用食伤生财、用比劫护财（方向="食官"）
          · 偏财格 → 同正财（方向="食官"）
          · 食神格 → 用比劫助食、用财泄食（方向="比财"）

        - 逆用（恶神）：制化用神，使其不伤日主
          · 七杀格 → 用食伤制杀、用印化杀（方向="食印"）
          · 伤官格 → 用印制伤、用财泄伤（方向="印财"）
          · 比肩格 → 用官制比、用食泄比（方向="官食"）
          · 劫财格 → 用官杀制劫（方向="官杀"）

    特殊说明：
        - 偏印用神方向与正印一致（"官比"），因《子平真诠》"印绶喜其生身，
          正偏同为美格"。财星是印格忌神（"印轻逢财，印格败也"），
          不应列为偏印用神方向。
        - 返回值为两个十神类别名的拼接，如"财印"表示喜财星和印星。

    Args:
        shishen: 十神名称（如"正官"/"七杀"/"正财"等10种具体十神之一）

    Returns:
        str: 用神方向，由两个十神类别名拼接而成，如"财印"/"食印"/"官比"等。
             未识别的十神返回"待定"。
    """
    direction_map = {
        # 顺用（善神）— 生护用神
        '正官': '财印',    # 官喜财生、印护（《子平真诠》"官喜财生，印护"）
        '正财': '食官',    # 财喜食伤生、官护（《子平真诠》"财喜食生"）
        '偏财': '食官',    # 同正财
        # 《子平真诠》"印绶喜其生身，正偏同为美格" — 偏印与正印用神方向一致
        # 财星是印格忌神（"印轻逢财，印格败也"），不应列为偏印用神方向
        '正印': '官比',    # 印喜官杀生、比劫护（《子平真诠》"印喜官生"）
        '偏印': '官比',    # 同正印
        '食神': '比财',    # 食喜比劫助、财泄（《子平真诠》"食喜比助"）
        # 逆用（恶神）— 制化用神
        '七杀': '食印',    # 杀喜食伤制、印化（《子平真诠》"杀喜食制"）
        '伤官': '印财',    # 伤喜印制、财泄（《子平真诠》"伤喜印制"）
        '比肩': '官食',    # 比喜官制、食泄
        '劫财': '官杀',    # 刃喜官杀制（《子平真诠》"刃喜官杀制"）
    }
    return direction_map.get(shishen, '待定')
