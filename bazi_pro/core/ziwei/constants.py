# ── 格局数据结构 ──────────────────────────────────────────────────────────────
@dataclass
class PatternCondition:
    """格局条件"""
    required: list[str] = field(default_factory=list)   # 必须满足条件
    bonus: list[str] = field(default_factory=list)      # 加分项
    breaking: list[str] = field(default_factory=list)   # 破格警示


@dataclass
class Pattern:
    """紫微斗数格局"""
    name: str
    level: PatternLevel  # excellent/good/neutral/caution
    description: str
    palaces: list[str] = field(default_factory=list)  # 涉及宫位
    conditions: PatternCondition | None = None
    source: str = ""  # 古籍出处


