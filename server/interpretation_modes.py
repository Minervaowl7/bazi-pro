def build_mode_prompt_suffix(mode: str) -> str:
    """为 LLM prompt 生成模式后缀指令"""
    if mode == "plain":
        return "请用通俗白话解读，避免命理术语，用比喻和日常语言让普通人能理解。"
    elif mode == "technical":
        return "请用纯命理术语解读，面向专业命理师，可直接引用古籍原文。"
    else:
        return "请用专业但易懂的方式解读，命理术语后附简短解释。"
